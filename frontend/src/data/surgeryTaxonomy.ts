export function subRegionById(
  id: string,
): { area: BodyArea | null; subRegion: SubRegion } | undefined {
  for (const area of BODY_AREAS) {
    for (const sr of area.subRegions) {
      if (sr.id === id) return { area, subRegion: sr };
    }
  }
  for (const sr of OTHER_GROUPS) {
    if (sr.id === id) return { area: null, subRegion: sr };
  }
  return undefined;
}

export function allOptions(): SurgeryOption[] {
  const fromAreas = BODY_AREAS.flatMap((a) =>
    a.subRegions.flatMap((sr) => sr.options),
  );
  const fromOther = OTHER_GROUPS.flatMap((sr) => sr.options);
  return [...fromAreas, ...fromOther];
}
