#!/usr/bin/env node
/**
 * Post-process .env files produced by generateDeveloperSecrets.sh.
 *
 *   --check-source-only
 *     Validate repo ai_scripts/.env has oe_ai_readonly + testing RW credentials.
 *
 *   --ai-scripts-export <dest.env>
 *     AI-safe export: strip SQL admin keys; DB_USER/PASSWORD = oe_ai_readonly (prod read-only);
 *     keep DB_USER_TESTING_RW for db-query default/--testing; DB_NAME=allaboard-testing.
 *
 *   --backend-testing <dest.env>
 *     Narrow backend export: DB_USER/PASSWORD = oe_testing_migrate, DB_NAME=allaboard-testing;
 *     strip SQL admin keys from dest.
 *
 *   --backend-prod-strip-admin <dest.env>
 *     Production backend export helper: remove AZURE_SQL_ADMIN_* / DB_ADMIN_* only.
 *
 * Env: AI_SCRIPTS_ENV_FILE = absolute path to repo ai_scripts/.env (required for checks / ai-scripts).
 */

const fs = require('fs');
const path = require('path');

const ADMIN_KEYS = new Set([
  'AZURE_SQL_ADMIN_USER',
  'AZURE_SQL_ADMIN_PASSWORD',
  'DB_ADMIN_USER',
  'DB_ADMIN_PASSWORD'
]);

function parseEnv(content) {
  /** @type {Record<string, string>} */
  const obj = {};
  const lines = content.split(/\r?\n/);
  for (const line of lines) {
    const t = line.trim();
    if (!t || t.startsWith('#')) continue;
    const eq = t.indexOf('=');
    if (eq <= 0) continue;
    const key = t.slice(0, eq).trim();
    let val = t.slice(eq + 1).trim();
    if (
      (val.startsWith('"') && val.endsWith('"')) ||
      (val.startsWith("'") && val.endsWith("'"))
    ) {
      val = val.slice(1, -1);
      val = val.replace(/\\n/g, '\n').replace(/\\'/g, "'");
    }
    obj[key] = val;
  }
  return obj;
}

function quoteEnv(val) {
  const s = String(val);
  if (/^[a-zA-Z0-9._:@%/+*^-]+$/.test(s)) return s;
  return "'" + s.replace(/'/g, "'\\''") + "'";
}

function serializeEnv(obj) {
  const keys = Object.keys(obj);
  const lines = [];
  for (const k of keys) {
    const v = obj[k];
    if (v === undefined || v === '') continue;
    lines.push(`${k}=${quoteEnv(v)}`);
  }
  return lines.join('\n') + '\n';
}

function mergeStripAdmin(destObj) {
  for (const k of ADMIN_KEYS) delete destObj[k];
}

function main() {
  const args = process.argv.slice(2);
  const repoAiEnv =
    process.env.AI_SCRIPTS_ENV_FILE ||
    path.join(__dirname, '.env');

  if (args[0] === '--check-source-only') {
    if (!fs.existsSync(repoAiEnv)) {
      console.error('Missing source env:', repoAiEnv);
      process.exit(1);
    }
    const src = parseEnv(fs.readFileSync(repoAiEnv, 'utf8'));
    const ru = (src.DB_USER || '').trim();
    const rp = src.DB_PASSWORD || '';
    const tw = src.DB_USER_TESTING_RW || '';
    const tp = src.DB_PASSWORD_TESTING_RW || '';
    if (!tw || !tp) {
      console.error(
        'Missing DB_USER_TESTING_RW / DB_PASSWORD_TESTING_RW in',
        repoAiEnv
      );
      process.exit(1);
    }
    if (ru !== 'oe_ai_readonly' || !rp) {
      console.error(
        'Source ai_scripts/.env must use DB_USER=oe_ai_readonly with DB_PASSWORD (AI / db-query prod read-only).'
      );
      console.error('Got DB_USER=', ru || '(empty)');
      process.exit(1);
    }
    console.log('OK: source ai_scripts/.env has readonly + testing RW credentials.');
    process.exit(0);
  }

  if (args[0] === '--ai-scripts-export' && args[1]) {
    if (!fs.existsSync(repoAiEnv)) {
      console.error('Missing AI_SCRIPTS_ENV_FILE / ai_scripts/.env:', repoAiEnv);
      process.exit(1);
    }
    const srcAi = parseEnv(fs.readFileSync(repoAiEnv, 'utf8'));
    const roPass = srcAi.DB_PASSWORD || '';
    const rwUser = srcAi.DB_USER_TESTING_RW || '';
    const rwPass = srcAi.DB_PASSWORD_TESTING_RW || '';
    if ((srcAi.DB_USER || '').trim() !== 'oe_ai_readonly' || !roPass) {
      console.error(
        'Repo ai_scripts/.env must have DB_USER=oe_ai_readonly and DB_PASSWORD for export.'
      );
      process.exit(1);
    }
    if (!rwUser || !rwPass) {
      console.error(
        'Repo ai_scripts/.env must define DB_USER_TESTING_RW / DB_PASSWORD_TESTING_RW.'
      );
      process.exit(1);
    }

    const destPath = path.resolve(args[1]);
    let destObj = {};
    if (fs.existsSync(destPath)) {
      destObj = parseEnv(fs.readFileSync(destPath, 'utf8'));
    }
    mergeStripAdmin(destObj);

    destObj.DB_SERVER = destObj.DB_SERVER || srcAi.DB_SERVER || '';
    destObj.DB_NAME = 'allaboard-testing';
    destObj.DB_USER = 'oe_ai_readonly';
    destObj.DB_PASSWORD = roPass;
    destObj.DB_USER_TESTING_RW = rwUser;
    destObj.DB_PASSWORD_TESTING_RW = rwPass;

    const header = [
      '# Generated by generateDeveloperSecrets.sh',
      '# Default & --testing: allaboard-testing + DB_USER_TESTING_RW (read/write on test DB).',
      '# --prod-readonly: allaboard-prod + oe_ai_readonly (read-only on prod).',
      '# SQL admin keys are never exported to this file.',
      ''
    ].join('\n');
    fs.writeFileSync(destPath, header + serializeEnv(destObj), 'utf8');
    console.log('Wrote AI-safe ai_scripts export:', destPath);
    process.exit(0);
  }

  if (args[0] === '--backend-testing' && args[1]) {
    if (!fs.existsSync(repoAiEnv)) {
      console.error('Missing source:', repoAiEnv);
      process.exit(1);
    }
    const srcAi = parseEnv(fs.readFileSync(repoAiEnv, 'utf8'));
    const rwUser = srcAi.DB_USER_TESTING_RW || '';
    const rwPass = srcAi.DB_PASSWORD_TESTING_RW || '';
    if (!rwUser || !rwPass) {
      console.error('Missing DB_USER_TESTING_RW / DB_PASSWORD_TESTING_RW in source.');
      process.exit(1);
    }

    const destPath = path.resolve(args[1]);
    if (!fs.existsSync(destPath)) {
      console.error('Missing dest file:', destPath);
      process.exit(1);
    }
    const destObj = parseEnv(fs.readFileSync(destPath, 'utf8'));
    mergeStripAdmin(destObj);
    destObj.DB_USER = rwUser;
    destObj.DB_PASSWORD = rwPass;
    destObj.DB_NAME = 'allaboard-testing';

    const header = [
      '# Generated by generateDeveloperSecrets.sh — backend → allaboard-testing + oe_testing_migrate',
      ''
    ].join('\n');
    fs.writeFileSync(destPath, header + serializeEnv(destObj), 'utf8');
    console.log('Wrote testing backend export:', destPath);
    process.exit(0);
  }

  if (args[0] === '--backend-prod-strip-admin' && args[1]) {
    const destPath = path.resolve(args[1]);
    if (!fs.existsSync(destPath)) {
      console.error('Missing dest file:', destPath);
      process.exit(1);
    }
    const destObj = parseEnv(fs.readFileSync(destPath, 'utf8'));
    mergeStripAdmin(destObj);
    const header = [
      '# Generated by generateDeveloperSecrets.sh — production DB (admin mirror keys stripped)',
      ''
    ].join('\n');
    fs.writeFileSync(destPath, header + serializeEnv(destObj), 'utf8');
    console.log('Stripped admin mirror keys from backend export:', destPath);
    process.exit(0);
  }

  console.error(
    'Usage: apply-testing-db-only-env.cjs --check-source-only | --ai-scripts-export <dest> | --backend-testing <dest> | --backend-prod-strip-admin <dest>'
  );
  process.exit(1);
}

main();
